package com.ifp.beans;

import java.util.List;

public class ReconBeans {
	
  public List reconhistory;
  public String username;
  public String gendate;
  public List binlist;
  
  

public String getUsername() {
	return username;
}

public void setUsername(String username) {
	this.username = username;
}

public String getGendate() {
	return gendate;
}

public void setGendate(String gendate) {
	this.gendate = gendate;
}

public List getBinlist() {
	return binlist;
}

public void setBinlist(List binlist) {
	this.binlist = binlist;
}

public List getReconhistory() {
	return reconhistory;
}

public void setReconhistory(List reconhistory) {
	this.reconhistory = reconhistory;
}
  
}
